public class RightAngleTriangle {
    public static void main(String args[]){
        int i,j;
       for(i=1;i<=5;i++)
       {
           for(j=i;j<=5;j++){
               System.out.print(" ");
           }
           for( i=1;i<=j;i++){
           System.out.print("*");
       }
       
       System.out.println(" ");
    }
    }
    
}
