public class InvertedRightAngleTriangle {
    public static void main(String args[]){
        for(int j=3;j>0;j--){
        for(int i=j;i>0;i--)
        {
            System.out.print("* ");
        }
        System.out.println(" ");
        }
        
        for(int j=0;j<=5;j++){
        for(int i=j;i<=5;i++)
        {
            System.out.print("*");
        }
        System.out.println(" ");
        }
        
    }
}
